   scarcity_sapphire_ore = d_sapphire*d_sapphire*d_sapphire,
   scarcity_ruby_ore= d_ruby*d_ruby*d_ruby,
   scarcity_amethyst_ore = d_amethyst*d_amethyst*d_amethyst,
   scarcity_emerald_ore = d_emerald*d_emerald*d_emerald,
}
